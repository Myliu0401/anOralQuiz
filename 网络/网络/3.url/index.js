/* 

    url
      统一资源定位符。是一个字符串，用于表达互联网中某个资源的位置

      一个完整的url地址由多个部分组成
        协议 + 主机 + 端口 + 路径 + 参数 + hash
        http://www.baidu.com:8080/news/2020?page=1&limt=10#78324868


      协议
        它表示客户端希望用什么方式和服务器沟通

      主机
        它表示客户端希望在哪台计算机上寻找资源（计算机在网络中的唯一编号）

      端口
        它表示客户端希望在那个应用程序中寻找资源
          每个服务器程序，都会监听一个或多个端口，只有找到对应的端口，才能找到服务程序

      路径 
        服务器上往往有很多资源，每个资源都有自己的访问路径

      参数
        表示数据，给服务的数据

      hash
        在网络通信中，hash没什么用，往往只作为锚链接出现


*/