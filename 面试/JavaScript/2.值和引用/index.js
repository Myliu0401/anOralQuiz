

/* 
     基本数据类型，一共有6种：
       string   symbol   number   boolean  undefined  null
       symbol为ES6新增的

      引用类型
        object


     基本类型 
        基本类型的数据为不可再细分的，由于基本类型的数据大小是固定的，所以基本类型的数据是存储在内存中的**栈**里
        栈的存取方式，为 **先进后出，后进先出**
 

      引用类型
        引用类型的数据可以向下拆分，拆分成多个原始值或引用值
        引用类型在内存中的大小是未知的，因为引用类型可以包含任何值，而不是一个特定的已知值，所以引用类型的数据都是
          存在**堆**。

*/